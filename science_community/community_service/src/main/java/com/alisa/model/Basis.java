package com.alisa.model;

public class Basis {
    private String basisId;

    private String basisDocumentId;

    private String basisCommunityId;

    public String getBasisId() {
        return basisId;
    }

    public void setBasisId(String basisId) {
        this.basisId = basisId;
    }

    public String getBasisDocumentId() {
        return basisDocumentId;
    }

    public void setBasisDocumentId(String basisDocumentId) {
        this.basisDocumentId = basisDocumentId;
    }

    public String getBasisCommunityId() {
        return basisCommunityId;
    }

    public void setBasisCommunityId(String basisCommunityId) {
        this.basisCommunityId = basisCommunityId;
    }
}