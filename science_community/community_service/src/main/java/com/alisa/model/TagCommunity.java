package com.alisa.model;

public class TagCommunity {
    private String tagCommunityId;

    private String tagId;

    private String communityId;

    public String getTagCommunityId() {
        return tagCommunityId;
    }

    public void setTagCommunityId(String tagCommunityId) {
        this.tagCommunityId = tagCommunityId;
    }

    public String getTagId() {
        return tagId;
    }

    public void setTagId(String tagId) {
        this.tagId = tagId;
    }

    public String getCommunityId() {
        return communityId;
    }

    public void setCommunityId(String communityId) {
        this.communityId = communityId;
    }
}